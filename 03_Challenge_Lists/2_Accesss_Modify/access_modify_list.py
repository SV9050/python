# Creating a grocery list
grocery_list = ['apples', 'bananas', 'carrots', 'milk', 'bread']

# Print
print ('Initial List', grocery_list)

# Replacing an item in the list using its index
grocery_list[1] =  'grapes'

# Printing the updated list
print ('Updated List', grocery_list)

# Printing the 3rd item of the list using its index value i.e 2
print ('3rd Item of the list', grocery_list[2])