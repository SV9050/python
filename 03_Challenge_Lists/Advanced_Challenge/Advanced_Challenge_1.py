# Creating a fruit list
fruits = ['banana', 'apple', 'grape', 'kiwi', 'orange']
print ( fruits )

# using the .sort to sort the items in ascending order
fruits.sort()

# printing the list in ascending order
print(fruits, 'in ascending order')

# using .reverse function to reverse the ascending order i.e now sorting in descending order
fruits.reverse()

# printing the list in descending order
print(fruits, 'in descending order')