# Create a set of numbers from 1 to 5
numbers = {1, 2, 3, 4, 5}

# Print the initial set
print(numbers)

# Add the number 6 to the set
numbers.add(6)

# Add the number 7 to the set
numbers.add(7)

# Remove the number 2 from the set
numbers.remove(2)

# Print the updated set
print(numbers)  # Display the final set
